#include <stddef.h>

char *ft_strtoupper(char *str)
{
    char *ptr = str;

    if (ptr != NULL)
    {
        while (*ptr != '\0')
        {
            if (*ptr >= 'a' && *ptr <= 'z')
            {
                *ptr -= 32;
            }
            ptr++;
        }
    }
    return (str);
}
